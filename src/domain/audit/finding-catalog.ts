import { FINDING_CODES, type FindingCode } from "./finding-codes";
import {
  createFindingIdentity,
  type Finding,
  type FindingCategory,
  type FindingEvidence,
  type FindingLocation,
  type FindingStateImpact,
  type Repairability,
  type Severity,
} from "../models/finding";
import type { RepairKind } from "../models/repair";

export interface FindingDefinition {
  readonly severity: Severity;
  readonly category: FindingCategory;
  readonly title: string;
  readonly description: string;
  readonly stateImpact: FindingStateImpact;
  readonly repairability: Repairability | "conditional";
  readonly recommendedRepair?: RepairKind;
}

type DefinitionTuple = readonly [
  Severity,
  FindingCategory,
  FindingStateImpact,
  Repairability | "conditional",
  string,
  string,
  RepairKind?,
];

function definition([
  severity,
  category,
  stateImpact,
  repairability,
  title,
  description,
  recommendedRepair,
]: DefinitionTuple): FindingDefinition {
  return {
    severity,
    category,
    stateImpact,
    repairability,
    title,
    description,
    ...(recommendedRepair === undefined ? {} : { recommendedRepair }),
  };
}

