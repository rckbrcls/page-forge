export interface CreateFindingOptions {
  readonly location?: FindingLocation;
  readonly targetIdentifier?: string;
  readonly evidence?: FindingEvidence;
  readonly repairability?: Repairability;
  readonly stateImpact?: FindingStateImpact;
  readonly recommendedRepair?: RepairKind;
  readonly description?: string;
}

export function createFinding(code: FindingCode, options: CreateFindingOptions = {}): Finding {
  const catalogued = FINDING_CATALOG[code];
  const repairability =
    options.repairability ?? (catalogued.repairability === "conditional" ? "none" : catalogued.repairability);
  const recommendedRepair =
    repairability === "automatic" ? (options.recommendedRepair ?? catalogued.recommendedRepair) : undefined;

  return {
    identity: createFindingIdentity(code, options.location, options.targetIdentifier),
    code,
    severity: catalogued.severity,
    category: catalogued.category,
    title: catalogued.title,
    description: options.description ?? catalogued.description,
    ...(options.location === undefined ? {} : { location: options.location }),
    ...(options.targetIdentifier === undefined ? {} : { targetIdentifier: options.targetIdentifier }),
    repairability,
    ...(recommendedRepair === undefined ? {} : { recommendedRepair }),
    revalidation: "not_compared",
    evidence: options.evidence ?? {},
    stateImpact: options.stateImpact ?? catalogued.stateImpact,
  };
}

export function isFindingCode(value: string): value is FindingCode {
  return (FINDING_CODES as readonly string[]).includes(value);
}

export const findingCatalog = FINDING_CATALOG;
export const createCatalogFinding = createFinding;
